                <p class="Texto">
                    <strong>A Descrição de um Problema</strong>
                </p>

                <p class="Texto">
                    Um problema deve ser descrito por intermédio de fatos verificáveis através dos quais ele se
                    manifesta como tal em relação ao ator que o declara. Esses fatos devem ter sua existência amplamente
                    aceita para que possam ser validados. A descrição de um problema é relativa ao ator que o declara: o
                    resultado de um jogo pode ser: um problema para um ator; uma ameaça para um segundo; um êxito para
                    um terceiro; e uma oportunidade para um quarto.
                </p>

                <p class="Texto">
                    A descrição de um problema deve precisar seu significado e torná-lo verificável mediante os fatos
                    que o evidenciam. Para tanto, ela precisa:
                </p>

                <p class="Texto pl-5">
                    <i class="fas fa-circle CorLaranja"></i> Reunir suas distintas interpretações possíveis em um só significado;<br>
                    <i class="fas fa-circle CorLaranja"></i> Precisar o que deve ser explicado: definir seu significado em termos de quantidade e de qualidade, de tempo e de localização;<br>
                    <i class="fas fa-circle CorLaranja"></i> Evidenciar o problema de uma forma monitorável, isto é, que permita o acompanhamento de sua
                    evolução;<br>
                    <i class="fas fa-circle CorLaranja"></i> Permitir que sejam previstas ou definidas fontes de verificação para a descrição construída.;
                </p>
                <hr>

                <p class="Texto">
                    <strong>A Explicação da Situação-Problema</strong>
                </p>

                <p class="Texto">
                    O conceito de diagnóstico aqui adotado está referido à forma como os atores participantes de um
                    jogo social observam e, portanto, explicam a realidade em que estão inseridos. Toda explicação pressupõe
                    reflexão. É ela que permite que o ator perceba possibilidades para transformar ou para manter uma dada
                    situação. Para explicar um problema, é necessário fazer uma distinção entre:
                </p>

                <p class="Texto pl-5">
                    <i class="fas fa-square CorLaranja"></i> Causas (o problema se deve a);<br>
                    <i class="fas fa-square CorLaranja"></i> Descrição (o problema se verifica através de);<br>
                    <i class="fas fa-square CorLaranja"></i> Consequências (o problema produz um impacto em).
                </p>

                <p class="Texto">
                    As causas imediatas da decisão de um jogo são as jogadas (fluxos ou movimentos). Para produzir
                    jogadas, é necessário ter capacidade de “produção” (acumulações ou potenciais que os jogadores possuem
                    ou utilizam). Mas as jogadas válidas são aquelas permitidas pelas regras estabelecidas para cada jogo.
                </p>

                <p class="Texto">
                    Assim, podemos afirmar que explicar uma situação ou um problema consiste em construirmos um
                    modelo explicativo de sua geração e de suas tendências. Logo, é necessário precisar as causas
                    diferenciando-as e indicando se são: fluxos (jogadas); acumulações (capacidades ou incapacidades); ou
                    regras.
                </p>
                <hr>
                <br>

                <p class="Texto">
                    <strong>A Diversidade das Explicações Situacionais</strong>
                </p>

                <p class="Texto">
                    Uma mesma realidade pode dar margem a diversas explicações. A carga de subjetividade que anima
                    o diagnóstico de situações implica:
                </p>

                <p class="Texto pl-5">
                    <i class="fa fa-arrow-right CorLaranja"></i> Respostas distintas para uma mesma pergunta;<br>
                    <i class="fa fa-arrow-right CorLaranja"></i> Perguntas sobre uma mesma situação (as perguntas relevantes são distintas para os distintos
                    jogadores);<br>
                    <i class="fa fa-arrow-right CorLaranja"></i> Seleção do foco de atenção sobre a realidade.
                </p>

                <p class="Texto">
                    Logo, para explicarmos uma realidade precisamos distinguir suas explicações. Para explicar uma
                    situação que me afeta, preciso compreender a explicação do outro, incluindo o que ele pensa sobre minha
                    explicação. Quanto maior for a nossa capacidade de entender a explicação do outro, maior será a
                    probabilidade de êxito de nossas jogadas, além de ser mais potente nossa ação.
                </p>
