                                entendido através de uma ferramenta simples e
                                muito útil para a análise de viabilidade política de
                                projetos e de ações de governo: o Triângulo de
                                Governo. Esse modelo é formado por três variáveis
                                interdependentes que se encontram nos seus
                                vértices, conforme ilustra a imagem. Correndo o
