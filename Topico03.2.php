                <h2 class="font-bold">3.2.2 - A Situação-Problema como Objeto do Planejamento Estratégico Governamental</h2>
                <hr class="pontLaranja">

                <p class="Texto">
                    Com a introdução do conceito de Governabilidade, podemos aprofundar o entendimento do
                    conceito de situação-problema e o papel que ela desempenha no PEG. Assim, partimos da ideia de que
                    qualquer situação pode ser entendida pelo ator com ela envolvido como o resultado, o “placar” de um
                    jogo, e que essa situação pode ser por ele encarada como um problema a resolver. Ou seja, o êxito em um
                    jogo será a solução de um problema ou a mudança do placar.
                </p>
                <br>

                <p class="Texto">
                    <strong>Situação, problema e situação-problema são, para todos os efeitos, sinônimos.</strong>
                </p>

                <p class="Texto">
                    Podemos entender a realidade social como um grande jogo integrado por muitos jogos parciais e
                    que possuem suas próprias regras, em que atores se veem envolvidos ou procuram se envolver. Em todos
                    os jogos, há alguns jogadores e alguns espectadores: nenhum ator social participa de todos os jogos. O
                    governante, o ator que planeja ou o encarregado da gestão de uma situação podem ser vistos como
                    jogadores que, com suas ações, produzem acumulações procurando alterar o resultado do jogo. É com
                    base nessas acumulações que eles podem ampliar, ou reduzir, sua capacidade de produzir novas jogadas e
                    alterar a situação inicial.
                </p>

                <p class="Texto">
                    Esse é o mecanismo básico através do qual se acumula ou se desacumula poder e se produz, ou não,
                    mudanças significativas sobre uma dada situação-problema. Observar a ação de governo, que gera
                    acúmulos de poder e resultados socialmente valorizados, é uma atividade-chave do PEG. Essa observação
                    exige:
                </p>

                <p class="Texto pl-5">
                    <i class="fas fa-square CorLaranja"></i> Identificação dos jogos e dos problemas em que o ator que planeja está envolvido;<br>
                    <i class="fas fa-square CorLaranja"></i> Determinação de sua relação com outros problemas e jogadores;<br>
                    <i class="fas fa-square CorLaranja"></i> Identificação de suas manifestações sobre a realidade ou das evidências que permitam verificar se
                    o problema está se agravando ou sendo solucionado pela ação de governo;<br>
                    <i class="fas fa-square CorLaranja"></i> Diferenciação entre as causas e as consequências dos vários jogos parciais.
                </p>
                <hr>
                <br>

                <p class="Texto">
                    <strong>Conceito de Problema (ou Situação-Problema)</strong>
                </p>

                <p class="Texto">
                    O elemento central do momento de Diagnóstico, mostrado no esquema que segue, é a produção de
                    um quadro que identifique e relacione entre si os problemas mais relevantes associados a uma dada
                    situação (ou instituição) em um determinado momento. Por exemplo, de um problema, ou de uma
                    situação-problema, podemos destacar o resultado de um jogo. O problema pode ser:
                </p>

                <p class="Texto pl-5">
                    <i class="fas fa-circle CorLaranja"></i><strong> Estruturado:</strong> quando é possível enumerar todas as variáveis que o compõem; precisar todas as relações entre as variáveis, fazer com que todos os jogadores reconheçam como tal a solução proposta.<br>

                    <i class="fas fa-circle CorLaranja"></i><strong> Quase estruturado:</strong> quando se podem enumerar apenas algumas das variáveis que o compõem, precisar apenas algumas das relações entre as variáveis, entender suas soluções como,necessariamente, situacionais, isto é, aceitáveis para um ator e vistas com restrições por outros.
                </p>

                <p class="Texto">
                    Os problemas produzidos pelos jogos sociais e por inclusão, os que são alvo do PEG são quase
                    estruturados. Um problema quase estruturado pode conter, como elementos parciais, problemas
                    estruturados, isto é, os problemas estruturados não existem na realidade social, salvo como aspectos ou
                    como partes de problemas quase estruturados.
                </p>
                <hr>
                <br>

                <p class="Texto">
                    <strong>Tipos de Problemas</strong>
                </p>

                <p class="Texto">
                    No jogo social, são produzidos três tipos de problema. Adotando como referencial o tempo, o
                    significado e a natureza do seu resultado para um determinado ator, o problema pode ser:
                </p>

                <p class="Texto pl-5">
                    <i class="fas fa-square CorLaranja"></i> Uma ameaça, isto é, um perigo potencial de perder algo conquistado ou agravar uma situação;<br>
                    <i class="fas fa-square CorLaranja"></i> Uma oportunidade, ou seja, a possibilidade de que o jogo social abra e sobre a qual o ator pode
                    agir para aproveitá-la com eficácia ou desperdiçá-la;<br>
                    <i class="fas fa-square CorLaranja"></i> Um obstáculo, isto é, uma deficiência passível de ser atacada através da adequada observação e
                    qualificação do jogo em curso.<br><br>
                    <strong><u>Podemos também classificar os problemas quanto:</u></strong>
                    <br>
                    <i class="fas fa-square CorLaranja"></i> Ao tempo, fazendo referência se estes são atuais ou potenciais;.<br>
                    <i class="fas fa-square CorLaranja"></i> À governabilidade, no que se refere ao controle, podendo ser total, baixo e fora de controle;<br>
                    <i class="fas fa-square CorLaranja"></i> À abrangência, fazendo menção se este é no âmbito nacional, local, específico, estadual ou
                    municipal;<br>
                    <i class="fas fa-square CorLaranja"></i> À estruturação, já que os problemas podem ser estruturados ou quase estruturados.
                </p>
                <hr>

                <p class="Texto">
                    O enfrentamento de problemas já criados ou presentes é um ato reativo. O enfrentamento das
                    ameaças e das oportunidades é um ato propositivo. A ação de caráter propositivo é um objetivo a ser
                    perseguido permanentemente pelo ator que busca melhores resultados e mais possibilidades de êxito. Tais
                    possibilidades, no entanto, não estão usualmente sob controle dos dirigentes públicos.
                </p>

                <p class="Texto">
                    Ao assumir a frente de uma organização ou instância de governo, a escala e a gravidade dos
                    problemas já detectados exigem soluções imediatas que costumam ser tão abrangentes, que a ação do
                    governante tende a ser de caráter reativo. Simultaneamente, entretanto, é necessário vislumbrar as novas
                    ameaças e oportunidades, procurando evitar o agravamento da situação (ação de caráter propositivo).
                </p>

                <!-- Fique Atento -->
                <div class="FiqueAtento">
                    <p class="FiqueAtentoTitulo"> ATENÇÃO</p>
                    <p class="Texto">
                        Um problema só existe quando uma situação adversa se torna foco de interesse
                        de um ator social. Isso ocorre devido ao mal-estar claramente percebido, que
                        produz o resultado de algum jogo em que ele está envolvido. Ou, em menor
                        medida, à identificação de que o jogo contém oportunidades cujo resultado pode
                        beneficiá-lo. Antes que isso ocorra, o resultado desse jogo é, para o ator, um
                        mal-estar impreciso ou uma mera necessidade sem demanda política.
                    </p>
                </div>
                <hr>

                <p class="Texto">
                    <strong><u>O diagnóstico da situação supõe:</u></strong>
                </p>

                <p class="Texto pl-5">
                    <i class="far fa-star CorLaranja"></i> Listar os problemas declarados pelos diversos atores sociais relevantes;<br>
                    <i class="far fa-star CorLaranja"></i> Avaliar os problemas segundo a perspectiva desses atores;<br>
                    <i class="far fa-star CorLaranja"></i> Situar os problemas no tempo e no espaço;<br>
                    <i class="far fa-star CorLaranja"></i> Verificar se existe complementaridade ou contradição entre os problemas declarados;<br>
                    <i class="far fa-star CorLaranja"></i> Identificar fatos que evidenciam e precisam a existência de problemas;<br>
                    <i class="far fa-star CorLaranja"></i> Levantar suas causas e consequências;<br>
                    <i class="far fa-star CorLaranja"></i> Selecionar as causas críticas que podem ser objeto de intervenção.
                </p>
                <hr>

                <p class="Texto">
                    <strong>Como Formular um Problema?</strong>
                </p>

                <p class="Texto">
