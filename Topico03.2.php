                <p class="Texto">
                    Um grupo político que pretende governar
                    formula um Projeto de Governo, que pode ser
                    entendido como o conjunto dos objetivos que ele
                    possui e que expressam os desejos da parte da
                    população que o elegeu, conferindo assim, Apoio
                    Político ao governo eleito. É intuitiva a ideia de que o apoio político, em qualquer mandato de um
                    governo eleito, começa alto e tende a diminuir. Assim como a de que um Projeto de Governo que não
                    pretende mudar a situação previamente existente – um projeto meramente “<strong>administrativo</strong>” – não irá
                    requerer uma alta governabilidade, pois não existirão muitos obstáculos à sua ação. Ao contrário, um
                    Projeto de Governo “transformador”, que expressa uma grande ambição do ator social de mudar a
                    situação previamente existente, exigirá alta governabilidade. Então, o grau de Governabilidade que um
                    ator social precisa para governar é inversamente proporcional ao Projeto de Governo, entendido este
                    como a ambição de mudar a situação previamente existente.
                </p>
                <br>

                <p class="Texto">
                    O sistema em que está inserido o Projeto de Governo não é passivo. As resistências e os apoios
                    indicam uma relação de forças que expressam a maior ou a menor sustentação política do que o ator
                    social que governa possui para implementar seu projeto político. Esse “<strong>Apoio Político</strong>” que a sociedade
                    confere ao governante e ao seu Plano de Governo pode ser entendido também como a força (que o ator
                    possui) para “<strong>fazer acontecer</strong>”, e está representado no vértice esquerdo do Triângulo. É também intuitiva
                    a ideia de que a Governabilidade é diretamente proporcional ao Apoio Político com que conta o ator
                    social.
                </p>

                <p class="Texto">
                    A equipe dirigente deve analisar, para cada projeto ou proposta de governo, qual é o efetivo apoio /
                    rejeição / desinteresse de atores políticos. No caso de um governo municipal, eles serão: o Prefeito; secretariado; movimentos sociais e sindicais; apoio partidário; opinião pública; legislativo; meios de
                    comunicação; formadores de opinião etc. O controle dos aspectos que integram o sistema depende da
                    capacidade que o dirigente possui para implementar seu projeto, construindo resultados, mudando a
                    realidade e ampliando, assim, sua Governabilidade.
                </p>
                <br>

                <p class="Texto">
                    Mas a análise deve informar, também, para cada projeto ou proposta de governo, qual é a
                    capacidade de governar da equipe dirigente, sua experiência de gestão, seus métodos de trabalho, sua
                    organização interna, suas habilidades pessoais, seu controle de meios para empreender o projeto, e seu
                    controle de recursos (tempo, conhecimento, financeiros, pessoal capacitado, capacidade para formar
                    opinião, para gerenciar ou para coordenar processos de trabalho, para gerar legislação ou
                    regulamentações, comunicação, mobilização de apoio).
                </p>

