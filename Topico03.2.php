                    ameaça ou uma intenção de não perder uma oportunidade. É necessário identificar e precisar problemas
                    atuais ou realmente potenciais, e evitar exercícios de futurologia e de imaginação dispersiva. Um
                    problema não é a “<strong>ausência de uma solução</strong>”. Exemplos de como formular os problemas imersos numa
