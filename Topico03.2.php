                <h2 class="font-bold">3.2.3 - O Fluxograma Explicativo da Situação Seleção de Nós Críticos</h2>
                <hr class="pontLaranja">

                <p class="Texto">
                    A visualização gráfica do resultado da aplicação da Metodologia de Diagnóstico de Situações é a
                    mesma proposta por Matus (1993) para o seu fluxograma explicativo situacional. O fluxograma é um
                    mapa cognitivo que busca sintetizar a discussão realizada por uma equipe sobre uma determinada
                    situação-problema. A sua estruturação é baseada no estabelecimento de relações de causa e de efeito entre
                    as variáveis que a conformam.
                </p>

                <!-- Fique Atento -->
                <div class="FiqueAtento">
                    <p class="FiqueAtentoTitulo"> ATENÇÃO</p>
                    <p class="Texto">
                        A elaboração, por uma equipe, de um modelo explicativo do problema que vá
                        além de um mero mapa cognitivo, faz com que ela construa uma síntese
                        rigorosa, seletiva e precisa, com base em nós explicativos concisos e
                        monitoráveis, o que facilita a permanente adaptação da explicação à mudança da
                        situação, facilita também a compreensão, restringindo a possibilidade de
                        ambiguidades devido ao uso de uma simbologia simples e uniforme.
                    </p>
                </div>
                <hr>
                <br>

                <p class="Texto">
                    <strong>Seleção de Nós Críticos</strong>
                </p>

                <p class="Texto">
                    O PEG exige o compromisso de atuar sobre problemas e situações como algo preciso e operacional.
                    De outra maneira, a reflexão como suporte à tomada de decisões não leva à ação efetiva nem se revela
                    prática.
                </p>

                <!-- Fique Atento -->
                <div class="FiqueAtento">
                    <p class="FiqueAtentoTitulo"> ATENÇÃO</p>
                    <p class="Texto">
                        <strong>Um fluxograma bem elaborado deve pelo menos atender às seguintes
                            perguntas:</strong><br><br>
                        ♯ Como e onde atuar para mudar a descrição de um problema?<br>
                        ♯ A mudança provocada será suficiente para alcançar os objetivos perseguidos?
                    </p>
                </div>

                <p class="Texto">
                    Os “nós explicativos” de um fluxograma (encadeamento de causas ou cadeias causais da situaçãoproblema)
                    sobre os quais podemos atuar com eficácia prática são denominados: “Nós Críticos”.
                </p>
                <hr>
                <br>

                <p class="Texto">
                    <strong>Critérios para Seleção de Nós Críticos</strong>
                </p>

                <p class="Texto">
                    Agora que sabemos o que são “Nós Críticos”, vamos ver que estes têm algumas condições a
                    cumprir. São elas:
                </p>

                <p class="Texto pl-5">
                    <i class="far fa-star CorLaranja"></i> Ser centros práticos de ação, isto é, o ator que declara o problema pode atuar prática, efetiva e
                    diretamente sobre eles sem precisar atuar sobre suas causas;<br>
                    <i class="far fa-star CorLaranja"></i> Ser nós explicativos que, se resolvidos ou “desatados”, terão alto impacto sobre o problema
                    declarado;<br>
                    <i class="far fa-star CorLaranja"></i> Ser centros oportunos de ação política, ou seja, seu ataque deve ser politicamente viável durante o
                    período definido pelo ator como relevante e a ação possui uma relação custo-benefício favorável.
                </p>
                <br>

                <p class="Texto">
                    Para melhor precisar um "nó crítico", devemos descrevê-lo de forma a torná-lo monitorável e
                    restringir a ambiguidade possível nas interpretações a ele referidas. Com a seleção dos "nós críticos" de
                    uma cadeia explicativa do problema (ou situação), o diagnóstico está concluído.
