                    completa na ação, e esse agir implica permanente avaliação e revisão do que foi planejado. Para obter confiabilidade, é necessário verificarmos a todo momento a qualidade da proposta, a sua consistência e
                    fundamentação, como também garantir a boa coordenação da formulação e da implementação.
                </p>
                <br>

                <p class="Texto">
