                    Na situação inicial, o ator declara sua insatisfação sobre uma dada realidade em um determinado
                    momento e, por isso, tem demandas a viabilizar, necessidades a satisfazer e problemas a resolver. Esse
                    ator constrói uma explicação que serve de base para a definição fundamentada de uma situação-objetivo.
                    Ela envolve a explicitação de um conjunto de resultados que se espera atingir ao final de um determinado
                    tempo e que resolverão os problemas iniciais formulados ou atenderão às demandas e às necessidades
                    identificadas.
                </p>
