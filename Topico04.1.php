                </p>
                <br>

                <p class="Texto">
                    A explicação situacional resultante é autorreferenciada, ou seja, ela é condicionada pelo ponto de
                    vista do ator. E, por isto, influenciada pelo tipo de inserção na realidade que possui o ator que planeja.
                </p>
                <br>

                <p class="Texto">
                    Uma explicação formulada por um ator social sobre um aspecto da realidade pode ser verificada ou
                    refutada apenas em função da maior ou menor capacidade de sua cadeia de argumentos em sustentar seus
                    questionamentos. Portanto, sempre haverá mais de uma visão acerca da realidade, sendo que os ideais de
                    objetividade e as distinções entre verdadeiro e falso perdem força no trabalho do analista e na reflexão
                    voltada para a ação que caracteriza o PEG.
                </p>
