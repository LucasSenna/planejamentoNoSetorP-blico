                    protagonista e não de simples observador, mas para isso precisa compreender a realidade em
                    transformação.
                </p>
                <br>

                <p class="Texto">
                    Como já destacado, cada “<strong>realidade</strong>” é percebida de modo distinto dependendo do ponto de
                    observação (valores, interesses, experiências prévias etc.) do ator que planeja (ou, simplesmente,
                    observa). Uma mesma realidade pode ser percebida de modo diferente dependendo de como está situado
                    um observador específico; quais são seus interesses e seus objetivos. Dessa forma, a análise de uma
                    determinada situação é uma apreciação da realidade que enfrenta um determinado ator a partir da sua
                    visão.
