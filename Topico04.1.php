                <p class="Texto">
                    <strong>Operações</strong>
                </p>

                <p class="Texto">
                    As operações podem ser entendidas como os grandes passos (conjunto de ações), ou como o
                    conjunto de condições que deve ser criado para a viabilização do plano, que são elaboradas como a
                    solução de cada "nó crítico" identificado num fluxograma explicativo. Solução essa a ser alcançada no
                    âmbito desse problema no prazo do plano. O conjunto deve ser suficiente para assegurar o cumprimento
                    do plano.
                </p>

                <p class="Texto">
                    As operações podem também ser entendidas como atos linguísticos enunciados no espaço das
                    declarações de compromissos, visando à mudança da realidade. Sua formulação deve iniciar por
                    <strong>“Comprometo-me a ... (fazer algo)”</strong>. Para tanto, as operações precisam ser:
