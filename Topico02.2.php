                    cotidiano de dirigentes públicos e outros profissionais em situação de governo. Seu criador foi o exministro
                    de Planejamento chileno do governo Allende, Carlos Matus. O método PES nasceu de um longo
                    processo de reflexão que teve lugar no período em que ele ficou preso em função do golpe militar, que
                    levou à morte do presidente Allende, em setembro de 1973. Essa reflexão o levou a formular uma crítica
                    ao planejamento governamental tradicional e propor um método alternativo que levasse em conta o
                    caráter situacional (situação do ator que planeja) e estratégico que deveria possuir o planejamento, em
